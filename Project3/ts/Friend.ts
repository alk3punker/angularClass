export class Friend {
    fullName: string;
    age: number;
    email: string;
    BestFriend: boolean;

    constructor (fullName:string,age:number,email:string,BestFriend:boolean) {
        this.fullName = fullName;
        this.age = age;
        this.email = email;
        this.BestFriend = BestFriend;
    }
}

